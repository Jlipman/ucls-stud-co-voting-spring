public class FullBallot {

    public String pres;
    public String vp;
    public String cu1;
    public String cu2;

    public FullBallot() {
        pres = "";
        vp = "";
        cu1 = "";
        cu2 = "";
    }

    public String getPres() {
        return pres;
    }

    public void setPres(String pres) {
        this.pres = pres;
    }

    public String getVp() {
        return vp;
    }

    public void setVp(String vp) {
        this.vp = vp;
    }

    public String getCu1() {
        return cu1;
    }

    public void setCu1(String cu1) {
        this.cu1 = cu1;
    }

    public String getCu2() {
        return cu2;
    }

    public void setCu2(String cu2) {
        this.cu2 = cu2;
    }

  
}
