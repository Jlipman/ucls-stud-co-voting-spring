public class AllSchoolBallot {

    private String pres;
    private String vp;
    private String cupres;
    private String cuvp;
    private String sec;
    private String tres;

    public AllSchoolBallot() {
        pres = "";
        vp = "";
        cupres = "";
        cuvp = "";
        sec = "";
        tres = "";
    }

    public String getPres() {
        return pres;
    }

    public void setPres(String pres) {
        this.pres = pres;
    }

    public String getVp() {
        return vp;
    }

    public void setVp(String vp) {
        this.vp = vp;
    }

    public String getCupres() {
        return cupres;
    }

    public void setCupres(String cupres) {
        this.cupres = cupres;
    }

    public String getCuvp() {
        return cuvp;
    }

    public void setCuvp(String cuvp) {
        this.cuvp = cuvp;
    }

    public String getSec() {
        return sec;
    }

    public void setSec(String sec) {
        this.sec = sec;
    }

    public String getTres() {
        return tres;
    }

    public void setTres(String tres) {
        this.tres = tres;
    }
    
    

  
}
